package OOP;

/**
 *
 * @author tungl
 */
public class Person {

    String identify;
    String address;

    public Person() {
    }

    public Person(String identify, String address) {
        this.identify = identify;
        this.address = address;
    }

    public String getIdentify() {
        return identify;
    }

    public void setIdentify(String identify) {
        this.identify = identify;
    }

    public String getAddress() {
        return address;
    }

    public void setAddress(String address) {
        this.address = address;
    }

    @Override
    public String toString() {
        return "Person{" + "identify=" + identify + ", address=" + address + '}';
    }

}
